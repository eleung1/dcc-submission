package org.icgc.dcc.submission.http.jersey;

import static com.google.common.net.HttpHeaders.ACCESS_CONTROL_ALLOW_CREDENTIALS;
import static com.google.common.net.HttpHeaders.ACCESS_CONTROL_ALLOW_HEADERS;
import static com.google.common.net.HttpHeaders.ACCESS_CONTROL_ALLOW_METHODS;
import static com.google.common.net.HttpHeaders.ACCESS_CONTROL_ALLOW_ORIGIN;

import java.io.IOException;

import javax.ws.rs.container.ContainerRequestContext;
import javax.ws.rs.container.ContainerResponseContext;
import javax.ws.rs.container.ContainerResponseFilter;
import javax.ws.rs.ext.Provider;

/**
 * Adds CORS headers for use with ICGC Docs, etc.
 * 
 * @see http://stackoverflow.com/questions/28065963/how-to-handle-cors-using-jax-rs-with-jersey
 */
@Provider
public class CorsFilter implements ContainerResponseFilter {

  @Override
  public void filter(ContainerRequestContext request, ContainerResponseContext response) throws IOException {
    response.getHeaders().add(ACCESS_CONTROL_ALLOW_ORIGIN, "*");
    response.getHeaders().add(ACCESS_CONTROL_ALLOW_HEADERS, "origin, content-type, accept, authorization");
    response.getHeaders().add(ACCESS_CONTROL_ALLOW_CREDENTIALS, "true");
    response.getHeaders().add(ACCESS_CONTROL_ALLOW_METHODS, "GET, OPTIONS, HEAD"); // Read-only methods
  }

}